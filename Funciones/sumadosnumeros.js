// Definimos una función utilizando function declaration
function declaration(num1, num2) {
// Realizamos la suma de los dos números
    var resultado = num1 + num2;  
// Devolvemos el resultado
    return resultado;
  }  
// Llamamos a la función y mostrar el resultado en la consola
  var numero1 = 12;
  var numero2 = 8;
  var resultadoSuma = declaration(numero1, numero2);
  console.log("El resultado de la suma es: " + resultadoSuma);
  