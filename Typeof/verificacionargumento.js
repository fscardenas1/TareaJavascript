// Definimos una función que recibe un argumento
function mostrarTipoDato(argumento) {
// Obtenemos el tipo de dato utilizando el operador typeof
    var tipoDato = typeof argumento;
// Mostramos en pantalla el tipo de dato en la consola
    console.log("El tipo de dato del argumento es: " + tipoDato);
}  
// Llamar a la función y pasar un valor como argumento
  mostrarTipoDato(88);
  